    private void applyLoanApplyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applyLoanApplyButtonActionPerformed

        if(!applyLoanApplyAmount.getText().isEmpty() && amount <= max_amount){
            try{

                amount = Double.valueOf(applyLoanApplyAmount.getText().trim());

                if(applyLoanLoanType.getSelectedIndex()==0) loanType = "personal";
                else if(applyLoanLoanType.getSelectedIndex()==1) loanType = "educational";
                else if(applyLoanLoanType.getSelectedIndex()==2) loanType = "housing";
                else if(applyLoanLoanType.getSelectedIndex()==3) loanType = "leasing";
                else if(applyLoanLoanType.getSelectedIndex()==4) loanType = "other";

                annualInterestRate = loanInterestRateDecider(loanType);

                if(applyLoanDuration.getSelectedIndex()==0) durationInMonths = 12;
                else if(applyLoanDuration.getSelectedIndex()==1) durationInMonths = 24;
                else if(applyLoanDuration.getSelectedIndex()==2) durationInMonths = 36;
                else if(applyLoanDuration.getSelectedIndex()==3) durationInMonths = 60;
                else if(applyLoanDuration.getSelectedIndex()==4) durationInMonths = 120;

                Loan loan = new Loan(accountNumber, amount, durationInMonths, annualInterestRate, 0, "pending");

                Class.forName("com.mysql.cj.jdbc.Driver");
                conn1 = DriverManager.getConnection("jdbc:mysql://localhost:3307/bank_system","root","");
                insert = conn1.prepareStatement("insert into loans (account_number,amount,duration,interest_rate,paid_amount,state) values (?,?,?,?,?,?)");
                insert.setInt(1,loan.getAccountNumber());
                insert.setDouble(2,loan.getAmount());
                insert.setInt(3,loan.getDurationInMonths());
                insert.setDouble(4,loan.getAnnualInterestRate());
                insert.setDouble(5,loan.getPaidAmount());
                insert.setString(6,loan.getState());
                insert.executeUpdate();

                Class.forName("com.mysql.cj.jdbc.Driver");
                conn4 = DriverManager.getConnection("jdbc:mysql://localhost:3307/bank_system","root","");
                updateStock = conn4.prepareStatement("update bank_money_stock set money_stock=?");
                updateStock.setDouble(1, money_stock - amount);
                updateStock.executeUpdate();

                JOptionPane.showMessageDialog(this, "You applied for the loan successfully. Please meet one of our managers to get it approved.");

                applyLoanUsername.setText("");
                applyLoanPassword.setText("");
                applyLoanAccountNumber.setText("");
                applyLoanMaximumAmount.setText("");
                applyLoanApplyAmount.setText("");
                applyLoanLoanType.setSelectedIndex(0);
                applyLoanDuration.setSelectedIndex(0);

                applyLoanUsername.requestFocus();

            }
            catch(SQLIntegrityConstraintViolationException g){
                JOptionPane.showMessageDialog(null, "Use another username and password.");
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(null, e);
                System.out.println(e);
            }
            catch(Exception f){
                JOptionPane.showMessageDialog(null, f);
                System.out.println(f);
            }
        }
        else if(amount >= max_amount){
            JOptionPane.showMessageDialog(null, "We can't lend you such a big amount of money.");
        }
        else if(applyLoanApplyAmount.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter how much you wish to borrow.");
        }
    }//GEN-LAST:event_applyLoanApplyButtonActionPerformed

