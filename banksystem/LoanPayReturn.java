    private void payReturnCheckButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payReturnCheckButtonActionPerformed
        try{

            String username = payReturnUsername.getText().trim();
            String password = String.valueOf(payReturnPassword.getPassword()).trim();

            Class.forName("com.mysql.cj.jdbc.Driver");
            conn1 = DriverManager.getConnection("jdbc:mysql://localhost:3307/bank_system","root","");
            select = conn1.prepareStatement("select * from clients");
            ResultSet rs = select.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            int c = rsmd.getColumnCount();

            while(rs.next()){
                for(int a=1; a<=c; a++){
                    if(rs.getString("username").equals(username) && rs.getString("password").equals(password)){

                        String accountNumberText = rs.getString("accountnumber");
                        System.out.println("Account Number : "+accountNumberText);
                        
                        accountNumber = Integer.valueOf(accountNumberText);

                        payReturnAccountNumber.setText(accountNumberText);
                      
                    }
                }
            }

            selectLoan = conn1.prepareStatement("select * from loans");
            ResultSet rs2 = selectLoan.executeQuery();
            ResultSetMetaData rsmd2 = rs2.getMetaData();
            int c2 = rsmd2.getColumnCount();
            DefaultTableModel df = (DefaultTableModel) payReturnTable.getModel();
            df.setRowCount(0);

            Vector v = new Vector();

            while(rs2.next()){
                for(int a=1; a<=c2; a++){
                    if(rs2.getInt("account_number")==accountNumber){

                        amount = rs2.getDouble("amount");
                        durationInMonths = rs2.getInt("duration");
                        annualInterestRate = rs2.getDouble("interest_rate");
                        paidAmount = rs2.getDouble("paid_amount");
                        state = rs2.getString("state");

                        Loan loan = new Loan(accountNumber, amount, durationInMonths, annualInterestRate, paidAmount, state);

                        v.add(loan.getAmount());
                        v.add(loan.getDurationInMonths());
                        v.add(loan.getAnnualInterestRate());
                        v.add(loan.getTotalReturn());
                        v.add(loan.getMonthlyReturn());
                        v.add(loan.getPaidAmount());
                        v.add(loan.getRemainingAmount());
                        v.add(loan.getState());
                      
                    }
                }
            }
            df.addRow(v);
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
            System.out.println(e);
        }
        catch(Exception f){
            JOptionPane.showMessageDialog(null, f);
            System.out.println(f);
        }
    }//GEN-LAST:event_payReturnCheckButtonActionPerformed

