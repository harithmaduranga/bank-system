    private void payReturnPayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payReturnPayButtonActionPerformed
        
        double payment = Double.valueOf(payReturnPaymentAmount.getText().trim());
        paidAmount += payment;
        remainingAmount -= payment; 

        Vector v2 = new Vector();

        Loan loan2 = new Loan(accountNumber, amount, durationInMonths, annualInterestRate, paidAmount, state);

        try{

            Class.forName("com.mysql.cj.jdbc.Driver");
            conn2 = DriverManager.getConnection("jdbc:mysql://localhost:3307/bank_system","root","");

            updateLoan = conn2.prepareStatement("update loans set paid_amount = ? where account_number = ?");
            updateLoan.setDouble(1,paidAmount);
            updateLoan.setInt(2,accountNumber);
            updateLoan.executeUpdate();

            selectStock = conn2.prepareStatement("select * from bank_money_stock");
            ResultSet rs3 = selectStock.executeQuery();
            ResultSetMetaData rsmd3 = rs3.getMetaData();
            int c3 = rsmd3.getColumnCount();

            while(rs3.next()){
                for(int b=1; b<=c3; b++){
                    money_stock = rs3.getDouble("money_stock");
                }
            }

            money_stock += payment;

            updateStock = conn2.prepareStatement("update bank_money_stock set money_stock=?");
            updateStock.setDouble(1, money_stock);
            updateStock.executeUpdate();

        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
            System.out.println(e);
        }
        catch(Exception f){
            JOptionPane.showMessageDialog(null, f);
            System.out.println(f);
        }

        v2.add(loan2.getAmount());
        v2.add(loan2.getDurationInMonths());
        v2.add(loan2.getAnnualInterestRate());
        v2.add(loan2.getTotalReturn());
        v2.add(loan2.getMonthlyReturn());
        v2.add(loan2.getPaidAmount());
        v2.add(loan2.getRemainingAmount());
        v2.add(loan2.getState());

        DefaultTableModel df3 = (DefaultTableModel) payReturnTable.getModel();
        df3.setRowCount(0);
        df3.addRow(v2);

        JOptionPane.showMessageDialog(null, "Your return payment is successfully done.");

    }//GEN-LAST:event_payReturnPayButtonActionPerformed

