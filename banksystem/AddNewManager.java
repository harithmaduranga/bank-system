    private void registerRegisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerRegisterButtonActionPerformed
        String name = registerName.getText().trim();
        String NIC = registerNIC.getText().trim();
        String telephone = registerTelephone.getText().trim();
        String username = registerUsername.getText().trim();
        String password = String.valueOf(registerPassword.getPassword()).trim();

        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn1 = DriverManager.getConnection("jdbc:mysql://localhost:3307/bank_system","root","");
            insert = conn1.prepareStatement("insert into managers (name,nic,telephone,username,password) values (?,?,?,?,?)");
            insert.setString(1,name);
            insert.setString(2,NIC);
            insert.setString(3,telephone);
            insert.setString(4,username);
            insert.setString(5,password);
            insert.executeUpdate();

            select = conn1.prepareStatement("select * from managers where username=?");
            select.setString(1, username);
            ResultSet rs = select.executeQuery();
            int managerID = rs.getInt("id");
            JOptionPane.showMessageDialog(this, "Manager added successfully. Your manager ID is"+managerID);

            registerName.setText("");
            registerNIC.setText("");
            registerTelephone.setText("");
            registerUsername.setText("");
            registerPassword.setText("");

            registerName.requestFocus();

        }
        catch(SQLIntegrityConstraintViolationException g){
            JOptionPane.showMessageDialog(null, "Use another username and password.");
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
            System.out.println(e);
        }
        catch(Exception f){
            JOptionPane.showMessageDialog(null, f);
            System.out.println(f);
        }

    }//GEN-LAST:event_registerRegisterButtonActionPerformed

