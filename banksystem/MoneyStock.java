    private void addMoneyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addMoneyButtonActionPerformed
        
        try{

            double addAmount = Double.valueOf(addingMoneyAmount.getText().trim());

            Class.forName("com.mysql.cj.jdbc.Driver");
            conn2 = DriverManager.getConnection("jdbc:mysql://localhost:3307/bank_system","root","");
            add = conn2.prepareStatement("update bank_money_stock set money_stock = ?");
            add.setDouble(1,stock+addAmount);
            add.executeUpdate();

            DecimalFormat formatter = new DecimalFormat("###,###,###,###.##");

            showCurrentStock();

            JOptionPane.showMessageDialog(null, "Money added successfully to the main stock.");
            
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
            System.out.println(e);
        }
        catch(Exception f){
            JOptionPane.showMessageDialog(null, f);
            System.out.println(f);
        }

    }//GEN-LAST:event_addMoneyButtonActionPerformed

