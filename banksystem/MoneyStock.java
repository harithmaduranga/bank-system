    public void showCurrentStock(){
        
        try{

            Class.forName("com.mysql.cj.jdbc.Driver");
            conn1 = DriverManager.getConnection("jdbc:mysql://localhost:3307/bank_system","root","");
            select = conn1.prepareStatement("select * from bank_money_stock");
            ResultSet rs = select.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            int c = rsmd.getColumnCount();

            DecimalFormat formatter = new DecimalFormat("###,###,###,###.##");

            while(rs.next()){
                for(int a=1; a<=c; a++){
                    stock = rs.getDouble("money_stock");
                    currentMoneyStock.setText("Rs. "+formatter.format(stock));
                }
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
            System.out.println(e);
        }
        catch(Exception f){
            JOptionPane.showMessageDialog(null, f);
            System.out.println(f);
        }

    }

