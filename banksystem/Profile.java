    private void profileCheckButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_profileCheckButtonActionPerformed
        try{

            String username = profileUsername.getText().trim();
            String password = String.valueOf(profilePassword.getPassword()).trim();

            Class.forName("com.mysql.cj.jdbc.Driver");
            conn1 = DriverManager.getConnection("jdbc:mysql://localhost:3307/bank_system","root","");
            select = conn1.prepareStatement("select * from clients");
            ResultSet rs = select.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            int c = rsmd.getColumnCount();

            while(rs.next()){
                for(int a=1; a<=c; a++){
                    if(rs.getString("username").equals(username) && rs.getString("password").equals(password)){

                        String accountNumber = rs.getString("accountnumber");
                        System.out.println("Account Number : "+accountNumber);

                        profileAccountNumber.setText(accountNumber);
                        profileName.setText(rs.getString("name"));
                        profileAddress.setText(rs.getString("address"));
                        profileNIC.setText(rs.getString("NIC"));
                        profileBirthday.setText(rs.getDate("birthday")+"");
                        profileTelephone.setText(rs.getString("telephone"));
                        profileOccupation.setText(rs.getString("occupation"));
                    }
                }
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
            System.out.println(e);
        }
        catch(Exception f){
            JOptionPane.showMessageDialog(null, f);
            System.out.println(f);
        }
    }//GEN-LAST:event_profileCheckButtonActionPerformed

